package DesignPattern.damn03_1;

public class Computer {
	private String CPU;
	private String GPU;

	public String getCPU() {
		return CPU;
	}

	public void setCPU(String CPU) {
		this.CPU = CPU;
	}

	public String getGPU() {
		return GPU;
	}

	public void setGPU(String GPU) {
		this.GPU = GPU;
	}
}
